// 페이지 당 질문 수
export const QUESTIONS_PER_PAGE = 7;

// 전체 페이지 수
export const TOTAL_PAGES = Math.ceil(DBTI_QUESTIONS.length / QUESTIONS_PER_PAGE);

/**
 * 특정 페이지의 질문들을 반환하는 유틸리티 함수
 * @param pageIndex - 페이지 인덱스 (0부터 시작)
 * @returns 해당 페이지의 질문 배열
 */
export const getQuestionsForPage = (pageIndex: number) => {
  const startIndex = pageIndex * QUESTIONS_PER_PAGE;
  const endIndex = Math.min(startIndex + QUESTIONS_PER_PAGE, DBTI_QUESTIONS.length);
  
  return DBTI_QUESTIONS.slice(startIndex, endIndex).map((text, index) => ({
    number: startIndex + index + 1,
    text
  }));
};